// Helper: compute Poseidon hash (NAPI direct call)
async function computePoseidonHash(inputs: string[]): Promise<string> {
  const result = napiGeneratePoseidonHash({ inputs });
  return result.hash;
}

// Helper: compute Anchor (NAPI direct call)
async function computeAnchor(secrets: Array<{ iss: string; sub: string; aud: string }>): Promise<string[]> {
  const result = napiGenerateAnchor({
    secrets,
  });
  return result.anchor;
}

// Helper: compute hanchor (same logic as smart contract initialize)
// h = hash(anchor[0], 0), h = hash(h, anchor[1]), ..., hanchor = h
async function computeHanchor(anchor: string[]): Promise<string> {
  let h = await computePoseidonHash([anchor[0], "0"]);
  for (let i = 1; i < anchor.length; i++) {
    h = await computePoseidonHash([h, anchor[i]]);
  }
  return h;
}

// Helper: return Anchor with hanchor appended
async function computeAnchorWithHanchor(secrets: Array<{ iss: string; sub: string; aud: string }>): Promise<string[]> {
  const anchor = await computeAnchor(secrets);
  const hanchor = await computeHanchor(anchor);
  return [...anchor, hanchor];
}

// Helper: generate ZK Proof (NAPI direct call)
async function generateZkProof(params: {
  pkPath: string;
  jwts: string[];
  pkOps: string[];
  merklePaths: string[][];
  leafIndices: number[];
  root: string;
  anchor: string[];
  hSignUserOp: string;
  random: string;
  audList: string[];
}): Promise<{ proofs: string[][]; sharedInputs: string[]; partialRhsList: string[] }> {
  const result = napiGenerateProof({
    pkPath: params.pkPath,
    jwts: params.jwts,
    pkOps: params.pkOps,
    merklePaths: params.merklePaths,
    leafIndices: params.leafIndices,
    root: params.root,
    anchor: params.anchor,
    hSignUserOp: params.hSignUserOp,
    random: params.random,
    audList: params.audList,
  });
  return result;
}

// Helper: convert NAPI sharedInputs(7) to contract ABI format (uint256[6], uint256[], uint256[], uint256[8][])
function buildContractSig(
  proofAndPublicInput: { sharedInputs: string[]; partialRhsList: string[]; proofs: string[][] },
  k: number,
): string {
  const napiShared = proofAndPublicInput.sharedInputs;
  // NAPI sharedInputs[6]: [hanchor(0), h_ctx(1), root(2), h_sign_userop(3), lhs(4), h_aud_list(5)]
  // jwt_exp, partial_rhs are separated into distinct fields
  const contractSharedInputs = [
    napiShared[0], // hanchor
    napiShared[1], // h_ctx
    napiShared[2], // root
    napiShared[3], // h_sign_userop
    napiShared[4], // lhs
    napiShared[5], // h_aud_list
  ];
  const jwtExpList = proofAndPublicInput.jwtExpList ?? Array(k).fill(napiShared[4]);
  const abiCoder = ethers.AbiCoder.defaultAbiCoder();
  return abiCoder.encode(
    ["uint256[6]", "uint256[]", "uint256[]", "uint256[8][]"],
    [contractSharedInputs, jwtExpList, proofAndPublicInput.partialRhsList, proofAndPublicInput.proofs],
  );
}

// Helper: convert RSA N to leaf input
function prepareLeafInput(iss: string, rsaN: string): string[] {
  const issFields = crypto.padAndStrToFieldsBN254(`"${iss}"`, MAX_ISS_LEN, PAD_CHAR);

  const formattedN = crypto.formattingModulorN(rsaN);
  const nDecimal = formattedN.map((value) => ethers.toBigInt(value));
  const leafInput = issFields.concat(nDecimal);

  return leafInput.map((value) => value.toString());
}

// Test Verifier wrapper interface
